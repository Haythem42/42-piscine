#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*str_duplicate;

	str_duplicate = malloc(ft_strlen(src) + 1);
	if (str_duplicate == NULL)
		return (NULL);
	ft_strcpy(str_duplicate, src);
	return (str_duplicate);
}
